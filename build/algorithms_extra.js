    function source_i(f, i) {
        return source(successor(f, i));
    }
    
    function shift_down(f, i, n) {
        while (i < n) {
            var i_big = i;
            var c1 = 2 * i + 1;
            var c2 = c1 + 1;
    
            if (c1 < n && source_i(f, c1) > source_i(f, i_big)) {
                i_big = c1;
            }
            
            if (c2 < n && source_i(f, c2) > source_i(f, i_big)) {
                i_big = c2;
            }
            
            if (i_big == i) {
                return;
            }
    
            iter_swap(successor(f, i), successor(f, i_big));
            i = i_big;
        }
    }
    
