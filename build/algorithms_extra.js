    function cycle_from(i, f) {
        var tmp = source(i);
        var j = i;
        var k = f(i);
        while ( ! equal(k, i)) {
            sink(j, source(k));
            j = k;
            k = f(k);
        }
        sink(j, tmp);
    }
    
    function rotate_cycles(f, m, l, from) {
        var d = gcd(distance(f, m), distance(m, l));
    
        while (d != 0) {
            --d;
            cycle_from(successor(f, d), from);
        }
        return successor(f, distance(m, l));
    }
    
